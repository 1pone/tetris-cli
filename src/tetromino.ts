export function createTetromino(type: TetrominoType): Tetromino {
  return {
    type,
    blocks: TETROMINO_SHAPES[type][0],
    pos: { x: 3, y: 0 },
    rotation: 0,
  };
}

export function getRandomTetromino(): Tetromino {
  const types: TetrominoType[] = ['I', 'O', 'T', 'S', 'Z', 'J', 'L'];
  const type = types[Math.floor(Math.random() * types.length)];
  return createTetromino(type);
}

export function rotateTetromino(tetromino: Tetromino): Tetromino {
  const shapes = TETROMINO_SHAPES[tetromino.type];
  const nextRotation = (tetromino.rotation + 1) % shapes.length;

  return {
    ...tetromino,
    blocks: shapes[nextRotation],
    rotation: nextRotation,
  };
}

export function getTetrominoBlocks(tetromino: Tetromino): Pos[] {
  return tetromino.blocks.map(block => ({
    x: tetromino.pos.x + block.x,
    y: tetromino.pos.y + block.y,
  }));
}

export function moveTetromino(tetromino: Tetromino, dx: number, dy: number): Tetromino {
  return {
    ...tetromino,
    pos: {
      x: tetromino.pos.x + dx,
      y: tetromino.pos.y + dy,
    },
  };
}
